package com.nawdroid.javafundamental.basic.perulangan;

public class For {
    public static void main (String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Angka : " + i);
        }

        /*
        * Berikut template penulisan 'For'
        *
        * for (initialization; termination; increment) {
            statement(s)
          }
        */
    }
}
